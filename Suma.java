public class Suma
{
    public static void main (String args [])
    {
        int num1        = 4;
        int num2        = 9;
        int respuesta   = 0;

        respuesta = num1 + num2;
        System.out.println ("El resultado de la suma con int es: " + respuesta);

        float fnum1      = -383;
        float fnum2      = 39;
        float frespuesta = 0;

        frespuesta = fnum1 + fnum2;
        System.out.println ("El resultado de la suma con float es: " + frespuesta);

    }
}