public class Operaciones5
{
    public static void main ( String args [])
    {
        int num1        = 8;
        int num2        = 6;
        int resultado   = 0;

        resultado = num1 + num2 /2;
        System.out.println ("Resultado incorrecto: " + resultado);

        resultado = (num1 + num2) /2;
        System.out.println ("Resultado correcto: " + resultado);

    }
}
